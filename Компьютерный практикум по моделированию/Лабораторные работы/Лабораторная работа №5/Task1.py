import numpy as np

# Данные из таблицы
x = np.array([-1.0, 0.0, 1.0, 2.0, 3.0, 4.0])
y = np.array([-0.5, 0.0, 0.5, 0.86603, 1.0, 0.86603])

# Построение матрицы A для многочлена первой степени (A * c = y)
A1 = np.vstack([np.ones_like(x), x]).T
c1 = np.linalg.lstsq(A1, y, rcond=None)[0]

# Вывод коэффициентов многочлена первой степени
print("Коэффициенты многочлена первой степени:")
print(f"c0 = {c1[0]}, c1 = {c1[1]}")

# Построение матрицы A для многочлена второй степени (A * c = y)
A2 = np.vstack([np.ones_like(x), x, x**2]).T
c2 = np.linalg.lstsq(A2, y, rcond=None)[0]

# Вывод коэффициентов многочлена второй степени
print("Коэффициенты многочлена второй степени:")
print(f"c0 = {c2[0]}, c1 = {c2[1]}, c2 = {c2[2]}")


